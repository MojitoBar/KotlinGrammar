class Calculator{
    fun sum(a:Int, b:Int):Int{
        return a + b
    }
}

fun Calculator.sum(a:Int, b:Int, c:Int):Int{
    return sum(a, b) + c
}

fun Calculator.minus(a:Int, b:Int):Int{
    return a - b
}

fun main(args:Array<String>){
    var calc = Calculator()
    println(calc.minus(10, 2))
    println(calc.sum(1, 2, 3))
}